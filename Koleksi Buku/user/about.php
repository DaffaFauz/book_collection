        <p>Perpustakaan adalah sebuah institusi yang menyediakan akses kepada masyarakat untuk memperoleh dan menggunakan berbagai jenis bahan pustaka, seperti buku, majalah, jurnal, dan media elektronik lainnya. Profil perpustakaan adalah deskripsi singkat yang menggambarkan karakteristik, layanan, dan tujuan dari perpustakaan tersebut. Sebagai pusat informasi, perpustakaan memiliki koleksi yang luas dan beragam, yang mencakup berbagai bidang pengetahuan dan minat. Profil perpustakaan mencakup informasi tentang jenis koleksi yang tersedia, seperti buku-buku referensi, fiksi, nonfiksi, dan koleksi khusus lainnya, seperti koleksi lokal, arsip, atau materi langka.</p>
